package com.devool.ucicareconnect.helper;

public class InstructionAttachmenthelper {
    String strDescription;
    String strFileName;
    String strExtension;
    String strAttachmentURL;

    public String getStrDescription() {
        return strDescription;
    }

    public void setStrDescription(String strDescription) {
        this.strDescription = strDescription;
    }

    public String getStrFileName() {
        return strFileName;
    }

    public void setStrFileName(String strFileName) {
        this.strFileName = strFileName;
    }

    public String getStrExtension() {
        return strExtension;
    }

    public void setStrExtension(String strExtension) {
        this.strExtension = strExtension;
    }

    public String getStrAttachmentURL() {
        return strAttachmentURL;
    }

    public void setStrAttachmentURL(String strAttachmentURL) {
        this.strAttachmentURL = strAttachmentURL;
    }
}
